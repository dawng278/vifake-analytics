#!/usr/bin/env python3
"""
Real-World Validation Evaluator — ViFake Analytics

Evaluates the live API against the manually-curated 80-sample real-world validation set.
This tests the model on text patterns representative of actual Vietnamese social media,
not generated by the synthetic data pipeline.

Usage:
    python scripts/eval_real_validation.py [--api http://localhost:8000] [--token vifake-demo-2024]
"""
import json
import sys
import time
import argparse
import urllib.request
from pathlib import Path
from collections import defaultdict

# ── Args ─────────────────────────────────────────────────────────────────────
ap = argparse.ArgumentParser()
ap.add_argument("--api",   default="http://localhost:8000", help="API base URL")
ap.add_argument("--token", default="vifake-demo-2024",     help="Bearer token")
ap.add_argument("--delay", default=1.5, type=float,        help="Seconds between requests (rate limit)")
args = ap.parse_args()

HEADERS = {"Authorization": f"Bearer {args.token}", "Content-Type": "application/json"}
BASE    = args.api
DATASET = Path(__file__).parent.parent / "data" / "real_validation" / "real_validation_set.jsonl"


# ── HTTP helpers ─────────────────────────────────────────────────────────────
def post_json(url, data):
    req = urllib.request.Request(
        url, data=json.dumps(data).encode(), headers=HEADERS, method="POST"
    )
    with urllib.request.urlopen(req, timeout=15) as r:
        return json.load(r)

def get_json(url):
    req = urllib.request.Request(url, headers=HEADERS)
    with urllib.request.urlopen(req, timeout=10) as r:
        return json.load(r)

def poll_job(job_id, max_wait=40):
    deadline = time.time() + max_wait
    attempt  = 0
    while time.time() < deadline:
        try:
            j = get_json(f"{BASE}/api/v1/job/{job_id}")
            if j.get("status") == "completed":
                return j.get("result", {})
            if j.get("status") == "failed":
                return None
        except Exception:
            pass
        delay = 0.4 if attempt < 4 else (0.8 if attempt < 10 else 1.5)
        time.sleep(delay)
        attempt += 1
    return None


# ── Load dataset ──────────────────────────────────────────────────────────────
samples = []
with open(DATASET, encoding="utf-8") as f:
    for line in f:
        line = line.strip()
        if line:
            samples.append(json.loads(line))

print(f"📂 Loaded {len(samples)} real-world validation samples")
label_counts = defaultdict(int)
for s in samples:
    label_counts[s["label"]] += 1
print(f"   Distribution: {dict(label_counts)}\n")


# ── Health check ─────────────────────────────────────────────────────────────
try:
    h = get_json(f"{BASE}/api/v1/health")
    print(f"✅ API healthy — version {h.get('version','?')}\n")
except Exception as e:
    print(f"❌ Cannot reach API: {e}\nStart the server first: uvicorn backend_services.api_gateway.main:app")
    sys.exit(1)


# ── Evaluation loop ───────────────────────────────────────────────────────────
results = []
for i, sample in enumerate(samples, 1):
    text     = sample["text"]
    expected = sample["label"]
    scenario = sample.get("scenario", "?")

    try:
        # Submit
        sub = post_json(f"{BASE}/api/v1/analyze", {
            "url":      "https://real-validation.vifake.local",
            "platform": "facebook",
            "priority": "high",
            "content":  text,
        })
        job_id = sub.get("job_id")
        if not job_id:
            results.append({"expected": expected, "predicted": "ERROR", "conf": 0.0, "text": text[:50]})
            continue

        # Poll
        res = poll_job(job_id)
        if res is None:
            results.append({"expected": expected, "predicted": "TIMEOUT", "conf": 0.0, "text": text[:50]})
            continue

        predicted = res.get("label", "?")
        conf      = res.get("confidence", 0.0)
        results.append({"expected": expected, "predicted": predicted, "conf": conf, "text": text[:60]})

        ok = "✅" if predicted == expected else "❌"
        print(f"[{i:2d}/{len(samples)}] {ok} exp={expected:<12} got={predicted:<12} conf={conf:.2f}  [{scenario}]")

    except Exception as e:
        results.append({"expected": expected, "predicted": "ERROR", "conf": 0.0, "text": text[:50]})
        print(f"[{i:2d}/{len(samples)}] ⚠️  Exception: {e}")

    time.sleep(args.delay)


# ── Metrics ───────────────────────────────────────────────────────────────────
print("\n" + "="*60)
print("EVALUATION RESULTS — Real-World Validation Set")
print("="*60)

labels = ["FAKE_SCAM", "SAFE", "SUSPICIOUS"]

# Confusion matrix counts
tp = defaultdict(int)
fp = defaultdict(int)
fn = defaultdict(int)
correct = 0

for r in results:
    exp  = r["expected"]
    pred = r["predicted"]
    if exp == pred:
        correct += 1
        tp[exp] += 1
    else:
        fp[pred] += 1
        fn[exp]  += 1

total = len(results)
accuracy = correct / total if total else 0

print(f"\nAccuracy: {accuracy:.1%}  ({correct}/{total})")
print("\nPer-class metrics:")
print(f"{'Class':<14}  {'Precision':>10}  {'Recall':>10}  {'F1':>8}  {'Support':>8}")
print("-" * 58)

f1s, weights = [], []
for label in labels:
    support = sum(1 for r in results if r["expected"] == label)
    if support == 0:
        continue
    p_denom = tp[label] + fp[label]
    precision = tp[label] / p_denom if p_denom else 0.0
    recall    = tp[label] / (tp[label] + fn[label]) if (tp[label] + fn[label]) else 0.0
    f1        = (2 * precision * recall / (precision + recall)) if (precision + recall) else 0.0
    f1s.append(f1)
    weights.append(support)
    print(f"{label:<14}  {precision:>10.1%}  {recall:>10.1%}  {f1:>8.3f}  {support:>8}")

macro_f1    = sum(f1s) / len(f1s) if f1s else 0.0
weighted_f1 = sum(f * w for f, w in zip(f1s, weights)) / sum(weights) if weights else 0.0
print("-" * 58)
print(f"{'Macro avg':<14}  {'':>10}  {'':>10}  {macro_f1:>8.3f}")
print(f"{'Weighted avg':<14}  {'':>10}  {'':>10}  {weighted_f1:>8.3f}")

# Error analysis
errors = [r for r in results if r["expected"] != r["predicted"] and r["predicted"] not in ("ERROR","TIMEOUT")]
if errors:
    print(f"\n⚠️  Misclassified samples ({len(errors)}):")
    for e in errors[:10]:
        print(f"  expected={e['expected']:<12} got={e['predicted']:<12} conf={e['conf']:.2f} | {e['text']}")

# Save results
out_path = DATASET.parent / "real_world_results.json"
with open(out_path, "w", encoding="utf-8") as f:
    json.dump({
        "summary": {
            "total": total,
            "correct": correct,
            "accuracy": round(accuracy, 4),
            "macro_f1": round(macro_f1, 4),
            "weighted_f1": round(weighted_f1, 4),
        },
        "label_distribution": dict(label_counts),
        "details": results,
    }, f, ensure_ascii=False, indent=2)
print(f"\n📄 Results saved to {out_path}")
print("\nNote: Expected synthetic F1=0.923, real-world F1 reflects domain shift (~8-12%).")
